import { useEffect, useState } from "react";
import { useLocation } from "react-router-dom";
import { getResult } from "../api/api";

export default function Result() {
  const { state } = useLocation();
  const [data, setData] = useState<any>(null);

  useEffect(() => {
    if (!state?.sessionId) return;

    getResult(state.sessionId)
      .then(res => setData(res.data))
      .catch(err => console.error(err));
  }, [state?.sessionId]);

  if (!data)
    return (
      <div className="min-h-screen flex items-center justify-center text-white bg-[#070B14]">
        Loading AI Evaluation...
      </div>
    );

  return (
    <div className="min-h-screen bg-[#070B14] text-white relative overflow-hidden">

      {/* BACKGROUND */}
      <div className="absolute inset-0 opacity-20 bg-[url('data:image/svg+xml;base64,PHN2ZyB3aWR0aD0iNjAiIGhlaWdodD0iNjAiIHZpZXdCb3g9IjAgMCA2MCA2MCI+PC9zdmc+')]" />
      <div className="absolute top-[-150px] left-[-150px] w-[400px] h-[400px] bg-purple-600 opacity-20 blur-[120px] rounded-full" />
      <div className="absolute bottom-[-150px] right-[-150px] w-[400px] h-[400px] bg-pink-600 opacity-20 blur-[120px] rounded-full" />

      <div className="relative z-10 max-w-6xl mx-auto px-6 pt-28 pb-20">

        {/* HEADER */}
        <div className="text-center mb-12">
          <h1 className="text-4xl font-bold mb-3">
            AI Interview Report
          </h1>

          <p className="text-gray-400">
            Performance analysis generated by AI
          </p>
        </div>

        {/* SCORE CARD */}
        <div className="bg-white/5 border border-white/10 backdrop-blur-xl rounded-2xl p-8 mb-10 text-center shadow-xl">

          <h2 className="text-2xl font-semibold mb-2">
            Overall Score
          </h2>

          <div className="text-6xl font-bold bg-gradient-to-r from-[#E83464] to-[#8E2DE2] bg-clip-text text-transparent">
            {data.averageScore}
          </div>

          <p className="mt-3 text-lg font-medium">
            {data.verdict}
          </p>

        </div>

        {/* STRENGTH + WEAK */}
        <div className="grid md:grid-cols-2 gap-6 mb-12">

          {/* STRENGTH */}
          <div className="bg-green-500/10 border border-green-400/20 p-6 rounded-xl">
            <h3 className="text-lg font-semibold mb-3 text-green-300">
              Strengths
            </h3>

            {(data.strongAreas || []).length > 0 ? (
              data.strongAreas.map((s: string, i: number) => (
                <p key={i} className="text-sm text-gray-300">
                  • {s}
                </p>
              ))
            ) : (
              <p className="text-gray-400">No strong areas detected</p>
            )}
          </div>

          {/* WEAK */}
          <div className="bg-red-500/10 border border-red-400/20 p-6 rounded-xl">
            <h3 className="text-lg font-semibold mb-3 text-red-300">
              Weak Areas
            </h3>

            {(data.weakAreas || []).length > 0 ? (
              data.weakAreas.map((w: string, i: number) => (
                <p key={i} className="text-sm text-gray-300">
                  • {w}
                </p>
              ))
            ) : (
              <p className="text-gray-400">No weak areas detected</p>
            )}
          </div>

        </div>

        {/* ANSWER BREAKDOWN */}
        <div>

          <h2 className="text-2xl font-semibold mb-6">
            Detailed Breakdown
          </h2>

          <div className="space-y-4">

            {data.answers.map((a: any, i: number) => (
              <div
                key={i}
                className="bg-white/5 border border-white/10 p-5 rounded-xl hover:bg-white/10 transition"
              >

                {/* QUESTION */}
                <p className="font-semibold mb-2">
                  Q{i + 1}: {a.question}
                </p>

                {/* ANSWER */}
                <p className="text-sm text-gray-300 mb-2">
                  Your Answer:{" "}
                  {Array.isArray(a.answer)
                    ? a.answer.join(", ")
                    : a.answer || "Not answered"}
                </p>

                {/* SCORE */}
                <div className="flex items-center justify-between">

                  <span
                    className={`text-sm font-semibold ${
                      a.score >= 7
                        ? "text-green-400"
                        : a.score >= 4
                        ? "text-yellow-400"
                        : "text-red-400"
                    }`}
                  >
                    Score: {a.score}/10
                  </span>

                </div>

                {/* FEEDBACK */}
                {a.feedback && (
                  <p className="text-xs text-purple-300 mt-2">
                    💡 {a.feedback.join(", ")}
                  </p>
                )}

              </div>
            ))}

          </div>
        </div>

      </div>
    </div>
  );
}